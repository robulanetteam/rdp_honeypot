# Public exponent: 0x5b7b88c0 (little-endian → значение 0x5b7b88c0)
TSSK_PUBLIC_EXPONENT = bytes([0x5b, 0x7b, 0x88, 0xc0])

# Целочисленные эквиваленты
TSSK_MODULUS_INT = int.from_bytes(TSSK_MODULUS, "little")
TSSK_PRIVATE_EXPONENT_INT = int.from_bytes(TSSK_PRIVATE_EXPONENT, "little")


def ts_sign(cert_info: bytes) -> bytes:
    """
    Подписывает CERT_INFO (поля Proprietary Certificate, см. MS-RDPBCGR
    5.3.3.1.4) well-known TS private key.

    Алгоритм:
        md5_hash = MD5(cert_info)                       # 16 байт
        blob = md5_hash || 0x00 || 0xFF * 45 || 0x01    # 63 байта
        sig  = (blob_as_int) ** d  mod  n               # RSA "raw"
        signature = sig.to_bytes(64, 'little')

    Возвращает 64-байтную сигнатуру.
    """
    from hashlib import md5
    h = md5(cert_info).digest()
    blob = h + b"\x00" + b"\xff" * 45 + b"\x01"
    assert len(blob) == 63, len(blob)
    m = int.from_bytes(blob, "little")
    s = pow(m, TSSK_PRIVATE_EXPONENT_INT, TSSK_MODULUS_INT)
    return s.to_bytes(64, "little")
